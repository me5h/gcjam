const solve = require('../../qualifying/parenting-partnering-returns/index');

test('[[360, 480, 0], [420, 540, 1], [600, 660, 2]]', () => {
    expect(solve([[360, 480, 0], [420, 540, 1], [600, 660, 2]])).toBe('CJC');
});

test('[[0, 1440, 0], [1, 3, 1], [2, 4, 2]]', () => {
    expect(solve([[0, 1440, 0], [1, 3, 1], [2, 4, 2]])).toBe('IMPOSSIBLE');
});

test('[[99, 150, 0], [1, 100, 1], [100, 301, 2], [2, 5, 3], [150, 250, 4]]', () => {
    expect(solve([[99, 150, 0], [1, 100, 1], [100, 301, 2], [2, 5, 3], [150, 250, 4]])).toBe('JCCJJ');
});

test('[[0, 720, 0], [720, 1440, 1]]', () => {
    expect(solve([[0, 720, 0], [720, 1440, 1]])).toBe('CC');
});
